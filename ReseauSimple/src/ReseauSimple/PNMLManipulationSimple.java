public class PNMLManipulationSimple extends PNMLManipulation{
	private int x,y;
	public PNMLManipulationSimple(PageHLAPI syspage,int x,int y) {
		super(syspage);
		this.x=x;
		this.y=y;
	}
	public void placeSimple(String name) {
		place(name,x,y,CSS2Color.BLACK, false);
		y+=100;
	}
	public void transitionSimple(String name) {
		transition(name,x,y,CSS2Color.BLACK);
		y+=100;
	}

}
